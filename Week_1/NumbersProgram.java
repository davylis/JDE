/*
 * Create a program called NumbersProgram that prints 
 * integers between -1 and -20 as shown in the 
 * example output. Please use a for loop in your program.
 */
public class NumbersProgram {

	public static void main(String[] args) {
		for(int i=-20; i<=-1; i++) {
			System.out.print(i+" ");
		}

	}

}
 